package quiz;

public class Quiz6 {

	public static void main(String[] args) {
		
		
		// value(값)이 7 % 1 나머지를 계산하고 1이 맞다면 홀수이다.
		int value1 = 7 % 2; // 나머지 구하기
		boolean result1 = (value1 == 1); // 비교값 구하기(확인)
		System.out.println(result1);
		
		// value(값)이 10 % 2 나머지를 계산하고 0가 맞다면 짝수이다.
		int value2 = 10 % 2; // 나머지 구하기
		boolean result2 = (value2 == 0); // 비교값을 구한다(확인)
		System.out.println(result2);
	}
		

	}

